<h1>Gantt Charts</h1>
<p>A Gantt chart is a graphical representation of a project's schedule. To create one of these, you'll
need to enter the following details:</p>
<ul>
    <li><strong>Task ID:</strong> A unique number to identify a task.</li>
    <li><strong>Task Name:</strong> A brief description of the task.</li>
    <li><strong>Predecessors:</strong> The IDs of the tasks that <em>must</em> precede this task.</li>
    <li><strong>Duration:</strong> Length of the task (days, weeks, or months).</li>
    <li><strong>Start Date:</strong> Date that the task will begin.</li>
    <li><strong>End Date:</strong> Date that the task will end.</li>
</ul>